    private void login(){
        String user = txtUsuario.getText();
        String pass = txtPass.getText();
        if (validarCampos()) {
            JOptionPane.showMessageDialog(null, "Campos Vacios");
        } else {
            if(user.equals("admin") & pass.equals("1234")){
                JOptionPane.showMessageDialog(null, "BIENVENIDO");
                JFramePrincipalAdmin principal = new JFramePrincipalAdmin();
                principal.setVisible(true);
                this.dispose();
            }else{
                ControlDocentes control = new ControlDocentes();
                
                Docente docente = control.validarSesion(user, pass);
                if (docente!=null) {    
                    JOptionPane.showMessageDialog(null, "BIENVENIDO, "+docente.getNombre()+"Este modulo estÃ¡ en mantenimiento");
//                    jFramePrincipal principal = new jFramePrincipal();
//                    principal.setVisible(true);
//                    this.dispose();
                } else 
                JOptionPane.showMessageDialog(null, "USUARIO O CONTRASEÃ‘A INCORRECTA");
                limpiar();
                
            }
        }
    }
    public boolean validarCampos(){
        if(txtPass.getText().equalsIgnoreCase("")) return true;
        if(txtUsuario.getText().equalsIgnoreCase("")) return true;
        return false;
    }
    public void limpiar(){
        txtPass.setText("");
        txtUsuario.setText("");
    }
