    private void actualizar() {

        String id = txtId.getText();
        if (controlDocente.buscar(id)) {
            if (!validar()) {
                Docente docente;
                String nombre = txtNombre.getText();
                String apellido = txtApellidos.getText();
                String email = txtEmail.getText();
                String pass = txtPass.getText();
                int tipo = comboTipo.getSelectedIndex();
                int estado = comboEstado.getSelectedIndex();
                int itemLinea = comboLinea.getSelectedIndex() - 1;
                

                if (tipo == 1) {
                    if (comboLinea.getSelectedIndex() == 0) {
                        Mensaje("Seleccione la Linea de Investigacion");
                        return;
                    } else {
                        LineaInvestigacion linea = controlLinea.buscarLinea(itemLinea);
                        docente = new DocenteEvaluador(id, tipo, estado, nombre, apellido, email, pass, linea);
                    }
                } else {
                    docente = new DocenteComite(id, tipo, estado, nombre, apellido, email, pass);
                }
                String mensaje = controlDocente.actualizarDocente(id, docente);
                Mensaje(mensaje);
                limpiar();

            } else {
                Mensaje("llenar todos los campos, ERROR");

            }
            
        } else {
            Mensaje("Codigo invalido");
        }
    }
    private void eliminar(){
        String id = txtId.getText();
        if(txtId.getText().equals("")){
            Mensaje("Digitar el Id del docente");
        }else{
            String mensaje = controlDocente.elimiarDocente(id);
            Mensaje(mensaje);
            tabla();
           
        }
    }
    private void buscar(){
        String id = txtId.getText();
        if(id.equals("")){
            Mensaje("Digite la Identificacion");
        }else{
            Docente docente = controlDocente.buscarDocente(id);
            if(docente!=null){
                llenarCampos(docente);
                Mensaje("Encontrado");
            }else{
                 Mensaje("identificacion no Encontrada");
            }
            
        }
    }
    private void llenarCampos(Docente docente){
        txtId.setText(docente.getId());
        txtNombre.setText(docente.getNombre());
        txtApellidos.setText(docente.getApellidos());
        txtEmail.setText(docente.getEmail());
        txtPass.setText(docente.getPassword());
        comboEstado.setSelectedIndex(docente.getEstado());
        comboTipo.setSelectedIndex(docente.getTipo());
        
    }
    private void guardar(){
        if(validar()){
            Mensaje("NINGUN CAMPO PUEDE QUEDAR VACIO");
        }else{
            Docente docente;
            String id = txtId.getText();
            String nombre = txtNombre.getText();
            String apellido = txtApellidos.getText();
            String email = txtEmail.getText();
            String pass = txtPass.getText();
            int tipo = comboTipo.getSelectedIndex();
            int estado = comboEstado.getSelectedIndex();
            int itemLinea = comboLinea.getSelectedIndex()-1;
            
            
            if (tipo == 1) {
                if (comboLinea.getSelectedIndex() == 0) {
                    Mensaje("Seleccione la Linea de Investigacion");
                    return;
                }else{
                    LineaInvestigacion linea = controlLinea.buscarLinea(itemLinea);
                    docente = new DocenteEvaluador(id, tipo, estado, nombre, apellido, email, pass, linea);
                }
                
            } else {
                docente = new DocenteComite(id, tipo, estado, nombre, apellido, email, pass);
            }
            String mensaje = controlDocente.addDocente(docente);
            Mensaje(mensaje);
            limpiar();
        }
        
    }
    private boolean validar(){
        if(txtId.getText().equals("")) return true;
        if(txtApellidos.getText().equals("")) return true;
        if(txtNombre.getText().equals("")) return true;
        if(txtEmail.getText().equals("")) return true;
        if(txtPass.getText().equals("")) return true;
        if(comboEstado.getSelectedIndex()==0){
            Mensaje("Seleccione el ESTADO");
            return true;       
        }
        if(comboTipo.getSelectedIndex()==0){
            Mensaje("Seleccione el TIPO");
            return true;       
        }

        return false;
    }
