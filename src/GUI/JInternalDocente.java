    private void tabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        String[] columnas = {"ID", "NOMBRE COMPLETO", "EMAIL", "TIPO DOCENTE", "ESTADO"};
        modelo.setColumnIdentifiers(columnas);
        ArrayList<Docente> lista = controlDocente.getLista();

        String[] fila = new String[columnas.length];
        for (Docente docente : lista) {
            fila[0] = docente.getId();
            fila[1] = docente.getNombreCompleto();
            fila[2] = docente.getEmail();
            fila[3] = docente.getTipoText();
            fila[4] = docente.getEstadoText();
            modelo.addRow(fila);
        }
        tabla.setModel(modelo);
    }

